package JavaBasic;

public class Exercise19 {

	public static void main(String[] args) {
		// The while Loop
		int Count = 0;
		while (Count < 25) {
			Count = Count + 5;
			System.out.println("Count is: " + Count);
		}
		int CountOnce = 25;
		System.out.println("==== Next Count ====");
		while (CountOnce < 50) {
			CountOnce = CountOnce + 5;
			System.out.println("CountOnce is " + CountOnce);

		}

	}

}
